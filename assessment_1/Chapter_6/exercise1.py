import math

# Ceil of a number
a = 2.3
ceil_a = math.ceil(a)
print(f"Ceil of {a} is {ceil_a}")

# Floor of a number
floor_a = math.floor(a)
print(f"Floor of {a} is {floor_a}")

# Factorial of a number
b = 5
factorial_b = math.factorial(b)
print(f"Factorial of {b} is {factorial_b}")

# Value of e^x
x = 23
exp_x = math.exp(x)
print(f"Value of e^{x} is {exp_x}")

# Square root of a number
c = 16
sqrt_c = math.sqrt(c)
print(f"Square root of {c} is {sqrt_c}")